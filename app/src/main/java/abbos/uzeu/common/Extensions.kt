    .replace("G'", "Ғ")
    .replace("SH", "Ш")
    .replace("CH", "Ч")
    .replace("YA", "Я")
    .replace("A", "А")
    .replace("B", "Б")
    .replace("D", "Д")
    .replace("E", "Й")
    .replace("F", "Ф")
    .replace("G", "Г")
    .replace("H", "Ҳ")
    .replace("I", "И")
    .replace("J", "Ж")
    .replace("K", "К")
    .replace("L", "Л")
    .replace("M", "М")
    .replace("N", "Н")
    .replace("O", "О")
    .replace("P", "П")
    .replace("Q", "Қ")
    .replace("R", "Р")
    .replace("S", "С")
    .replace("T", "Т")
    .replace("U", "У")
    .replace("X", "Х")
    .replace("V", "В")
    .replace("Y", "Й")
    .replace("Z", "З")
    .replace("'", "Ъ")